/*Faça um programa em Java (Tabuada.java) para imprimir a tabuada de 10. */

class Main {
  public static void main(String[] args) {
    int tabuada = 10;
    int x;

    for(x=0; x<=10; x++)
    {
      System.out.println(x * tabuada);
    }
  }
}
